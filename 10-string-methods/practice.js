// Day 10 - JavaScript String Methods

let text = "JavaScript Learning";

// length
console.log("Length:", text.length);

// toUpperCase()
console.log("Uppercase:", text.toUpperCase());

// toLowerCase()
console.log("Lowercase:", text.toLowerCase());

// includes()
console.log("Includes 'Script':", text.includes("Script"));

// indexOf()
console.log("Index of Learning:", text.indexOf("Learning"));

// slice()
console.log("Slice:", text.slice(0, 10));

// replace()
console.log("Replace:", text.replace("Learning", "Journey"));

// trim()
let spaceText = "   Hello JS   ";
console.log("Trim:", spaceText.trim());
