// Day 4 - JavaScript Conditionals

let age = 20;

// if statement
if (age >= 18) {
  console.log("You are eligible to vote");
}

// if-else statement
let number = 5;

if (number % 2 === 0) {
  console.log("Even number");
} else {
  console.log("Odd number");
}

// else-if example
let marks = 75;

if (marks >= 90) {
  console.log("Grade A");
} else if (marks >= 70) {
  console.log("Grade B");
} else {
  console.log("Grade C");
}
