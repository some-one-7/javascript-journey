// Day 6 - JavaScript Functions

// Simple function
function greet() {
  console.log("Hello, welcome to JavaScript!");
}

greet();

// Function with parameters
function add(a, b) {
  console.log("Sum:", a + b);
}

add(5, 3);

// Function with return value
function multiply(x, y) {
  return x * y;
}

let result = multiply(4, 6);
console.log("Multiplication:", result);
