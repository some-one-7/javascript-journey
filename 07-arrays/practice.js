// Day 7 - JavaScript Arrays

// Creating an array
let fruits = ["Apple", "Banana", "Mango"];

// Accessing elements
console.log(fruits[0]);
console.log(fruits[1]);

// Adding an element
fruits.push("Orange");
console.log(fruits);

// Removing last element
fruits.pop();
console.log(fruits);

// Array length
console.log("Array length:", fruits.length);
